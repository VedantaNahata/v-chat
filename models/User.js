const mongoose = require("mongoose");

const usersSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

// Creates a document in the database
const userCollection = new mongoose.model("users", usersSchema);
module.exports = userCollection;
